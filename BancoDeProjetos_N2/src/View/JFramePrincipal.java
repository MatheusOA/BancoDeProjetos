    public void atualizarComponentes() {
        this.tb_Projetos.setModel(new ProjetoTableModel(new ProjetoDAO().ListarTodos()));
        this.tb_Tarefas.setModel(new TarefasTableModel(new TarefasDAO().ListarTodos()));
        this.tb_Clientes.setModel(new ClienteTableModel(new ClienteDAO().ListarTodos()));
        this.tb_Usuarios.setModel(new UsuarioTableModel(new UsuarioDAO().ListarTodos()));
        this.tb_Hardware.setModel(new HardwareTableModel(new HardwareDAO().ListarTodos()));

        cbx_ClienteFinalProjeto.removeAllItems();
        for (int i = 0; i < clienteDAO.ListarClientesFinais().size(); i++) {
            cbx_ClienteFinalProjeto.addItem(clienteDAO.ListarClientesFinais().get(i));
        }

        cbx_ClienteIntegradorProjeto.removeAllItems();
        for (int i = 0; i < clienteDAO.ListarClientesIntegradores().size(); i++) {
            cbx_ClienteIntegradorProjeto.addItem(clienteDAO.ListarClientesIntegradores().get(i));
        }

        cbx_HardwareEspecificadoProjeto.removeAllItems();
        for (int i = 0; i < hardwareDAO.ListarHardwares().size(); i++) {
            cbx_HardwareEspecificadoProjeto.addItem(hardwareDAO.ListarHardwares().get(i));
        }

        cbx_ProjetoTarefas.removeAllItems();
        for (int i = 0; i < projetoDAO.ListarProjetos().size(); i++) {
            cbx_ProjetoTarefas.addItem(projetoDAO.ListarProjetos().get(i));
        }

        cbx_ResponsavelTarefas.removeAllItems();
        for (int i = 0; i < usuarioDAO.ListarUsuarios().size(); i++) {
            cbx_ResponsavelTarefas.addItem(usuarioDAO.ListarUsuarios().get(i));
        }

    }

