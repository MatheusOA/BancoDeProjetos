    private void tb_ToDoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_ToDoMouseClicked
        this.btn_Cancelar.setVisible(false);
        this.btn_Salvar.setVisible(false);
        this.btn_Novo.setVisible(false);
        this.btn_Voltar.setVisible(true);
        this.btn_Alterar.setVisible(true);
        this.btn_Deletar.setVisible(true);
        this.btn_Kanban.setVisible(false);

        alterarEnabled(false);

        CardLayout card = (CardLayout) Root.getLayout();
        card.show(Root, "pan_DetalhesTarefas");
        TarefasDAO tarefasDAO = new TarefasDAO();

        String kanban = String.valueOf(tb_ToDo.getValueAt(tb_ToDo.getSelectedRow(), KanbanToDoTableModel.COL_ID_KANBAN));
        kanban = kanban.substring(kanban.indexOf('>') + 1, kanban.indexOf('-')).trim();
        Tarefas tarefaAtual = tarefasDAO.ListarSelecionado(Integer.parseInt(kanban));

        tbx_IdTarefas.setText(String.valueOf(tarefaAtual.getId()));
        tbx_TituloTarefas.setText(String.valueOf(tarefaAtual.getNome()));
        tbx_DescricaoTarefas.setText(String.valueOf(tarefaAtual.getDescricao()));
        String dataInicio = String.valueOf(tarefaAtual.getDataInicio()).replaceAll("-", "/");
        String[] sInicio = dataInicio.split("/");
        String novaDataInicio = sInicio[2] + "/" + sInicio[1] + "/" + sInicio[0];
        jFormattedTextField1.setText(novaDataInicio);

        String dataFim = String.valueOf(tarefaAtual.getDataFim()).replaceAll("-", "/");
        String[] sFim = dataFim.split("/");
        String novaDataFim = sFim[2] + "/" + sFim[1] + "/" + sFim[0];
        tbx_DataInicioTarefas2.setText(novaDataFim);

        cbx_ResponsavelTarefas.setSelectedItem(String.valueOf(tarefaAtual.getNomeResponsavel()));
        cbx_ProjetoTarefas.setSelectedItem(String.valueOf(tarefaAtual.getNomeProjeto()));
        tbx_PorcentagemAndamentoTarefas.setText(String.valueOf(tarefaAtual.getAndamento()));
        cbx_StatusTarefas.setSelectedItem(String.valueOf(tarefaAtual.getStatus()));

    }//GEN-LAST:event_tb_ToDoMouseClicked

