    private void DetalharTarefas() {
        this.btn_Cancelar.setVisible(false);
        this.btn_Salvar.setVisible(false);
        this.btn_Novo.setVisible(false);
        this.btn_Voltar.setVisible(true);
        this.btn_Alterar.setVisible(true);
        this.btn_Deletar.setVisible(true);
        this.btn_Kanban.setVisible(false);

        alterarEnabled(false);

        CardLayout card = (CardLayout) Root.getLayout();
        card.show(Root, "pan_DetalhesTarefas");

        tbx_IdTarefas.setText(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_ID_TAREFAS).toString());
        tbx_TituloTarefas.setText(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_NOME_TAREFAS).toString());

        String dataInicio = tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_DATAINICIO_TAREFAS).toString().replaceAll("-", "/");
        String[] sInicio = dataInicio.split("/");
        String novaDataInicio = sInicio[2] + "/" + sInicio[1] + "/" + sInicio[0];
        jFormattedTextField1.setText(novaDataInicio);

        String dataFim = tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_DATAFIM_TAREFAS).toString().replaceAll("-", "/");
        String[] sFim = dataFim.split("/");
        String novaDataFim = sFim[2] + "/" + sFim[1] + "/" + sFim[0];
        tbx_DataInicioTarefas2.setText(novaDataFim);

        cbx_ResponsavelTarefas.setSelectedItem(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_NOMERESPONSAVEL_TAREFAS));
        cbx_ProjetoTarefas.setSelectedItem(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_NOMEPROJETO_TAREFAS));
        tbx_PorcentagemAndamentoTarefas.setText(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_ANDAMENTO_TAREFAS).toString());
        AtualizarBarraAndamento();
        cbx_StatusTarefas.setSelectedItem(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_STATUS_TAREFAS).toString());

        tbx_DescricaoTarefas.setText(tb_Tarefas.getValueAt(tb_Tarefas.getSelectedRow(), TarefasTableModel.COL_DESCRICAO_TAREFAS).toString());

    }

